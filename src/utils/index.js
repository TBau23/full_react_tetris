export const random = (min, max) => {
    return Math.floor(Math.random() * (max - min + 1)) + min
} // array of 6
// floor( .99 * (5 - 1 + 1)) => .99 * 5 => 4.95 => 4 + 1

export const gridDefault = () => {
    const rows = 18;
    const cols = 10;
    const grid = [];
    
    for(let i = 0; i < rows; i++ ) {
        grid.push([]);
        let currentRow = grid[i];
        for(let j = 0; j < cols; j++ ) {
            currentRow.push(0);
        }
    }
    return grid
}

